#wap to accept two numbers from user and find sum, difference, multiplication and power

a=int(input("Enter any number:"))
b=int(input("Enter any number"))
sum = a + b
difference = a - b
product = a * b
power = a ** b

# print results
print(f"Sum: {a} + {b} = {sum}")
print(f"Difference: {a} - {b} ={difference})")
print(f"Product: {a} * {b} = {product}")
print(f"Power: {a} ** {b}={power}")
